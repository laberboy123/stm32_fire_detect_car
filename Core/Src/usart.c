void have_OK(uint8_t* Buffer)
{
    if(strstr((char *)Buffer, "OK")) {
        Flag_UART2_receive_OK = 1;
        memset(Buffer, '\0', BufferLength);
    }
}

void Process_UART2_Data(uint8_t *data, uint16_t length)
{
    printf("Size2: %d\r\n", length);
    have_OK(data); // Include OK or not
    if(strstr((char *)UART2_RXBuffer, "CHANGE_MODE"))
    {
        // 切换模式
        if (currentMode == TRACE_MODE) {
            currentMode = CONTROL_MODE; // 远程遥控模式
        } else {
            currentMode = TRACE_MODE;   // 循迹模式
        }
        printf("Mode changed to: %s\n", (currentMode == TRACE_MODE) ? "TRACE_MODE" : "CONTROL_MODE");
        memset(UART2_RXBuffer, '\0', BufferLength);
    }
}

